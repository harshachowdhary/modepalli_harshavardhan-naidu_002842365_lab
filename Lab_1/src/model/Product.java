public class Product {
   
    private String name;
    private String descr;
    private String avainNum;
    private String price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescr() {
        return descr;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }

    public String getAvainNum() {
        return avainNum;
    }

    public void setAvainNum(String avainNum) {
        this.avainNum = avainNum;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
    
    
    
}
